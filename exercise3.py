#Task2: Bank Interest. If you have a savings account with a 
# particular initial amount and a fixed yearly interest rate, 
# calculate the total amount in your account after a year. 
# So if you had $10,000 at a 7% interest write code that would 
# tell us the amount at the end of the year. For the example 
# the expected outcome would be $10,700.

initial_amount = 10000
interest_amount = 0.07
end_of_year_amount = initial_amount * (1 + interest_amount)
print(end_of_year_amount)